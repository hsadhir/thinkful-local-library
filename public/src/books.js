// anonymous function expression
const findAuthorById = function (authors, id) {
  // Have used a for/of loop to find author's id deliberately.
  // this could be done using a find, which I have used in a similar function below.
  for (const author of authors) {
    if (author.id === id) return author;
  }
  return;
}

function findBookById(books, id) { return books.find(book => book.id === id); }

